module.exports = {
    add (a: number, b: number): number {
        return a + b;
    },

    subtraction (a: number, b: number): number {
        return a - b;
    },

    multiplication (a: number, b: number): number {
        return a * b;
    },

    division (a: number, b: number): number {
        return a / b;
    },

};